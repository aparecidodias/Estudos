x = input("digite algo")
print("É um número?" , x.isnumeric())
print("É um alfanumerico  (numeros e letras)? ", x.isalnum())
print("É um alfabetico (letras)?" , x.isalpha())
print("Está em minusculo?" , x.islower())
print("Está em maiusculo?" , x.isupper())
print("É somente espaço?" , x.isspace())
print("Está capitalizado (possui maiusculas = minusculas)" , x.istitle())

